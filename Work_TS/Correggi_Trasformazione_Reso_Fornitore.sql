SELECT DO11_DDT.DO11_DOCUM_MG36, DO11_DDT.DO11_NUMDOC, DO11_RESO.DO11_DOCUM_MG36,  DO11_RESO.DO11_NUMDOC,
DO30_CODART_MG66, DO33_FLGAGGQTATRASF, DO33_FLGSTORNOQTAVAL
FROM DO11_DOCTESTATA DO11_DDT
INNER JOIN DO30_DOCCORPO ON
DO11_DDT.DO11_DITTA_CG18=DO30_DITTA_CG18 AND
DO11_DDT.DO11_NUMREG_CO99=DO30_NUMREG_CO99
INNER JOIN DO31_DOCCORPOORD ON
DO31_DITTA_CG18=DO30_DITTA_CG18 AND
DO31_NUMREG_CO99=DO30_NUMREG_CO99 AND
DO31_PROGRIGA=DO30_PROGRIGA
INNER JOIN DO33_DOCCORPORIF ON
DO33_DITTA_CG18=DO30_DITTA_CG18 AND
DO33_NUMREGRIF_CO99=DO30_NUMREG_CO99 AND
DO33_PROGRIGARIF_DO30=DO30_PROGRIGA 
INNER JOIN DO11_DOCTESTATA DO11_RESO ON
DO11_RESO.DO11_DITTA_CG18=DO33_DITTA_CG18 AND
DO11_RESO.DO11_NUMREG_CO99=DO33_NUMREG_CO99
WHERE DO11_DDT.DO11_DOCUM_MG36 = 'F-CA'
AND DO11_RESO.DO11_DOCUM_MG36='F-BR'
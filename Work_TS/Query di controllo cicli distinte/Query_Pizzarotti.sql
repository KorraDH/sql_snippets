SELECT 
--COUNT(*)
DO11_DOCUM_MG36, DO11_NUMDOC, DO11_DATADOC, DO30_CODART_MG66, DO30_PREZZO1, DO30_QTA1, DO30_IMPORTO, DO30_IMPNETSCP, (DO30_IMPNETSCP/DO30_QTA1),  ((DO30_IMPNETSCP/DO30_QTA1)-DO30_PREZZO1)
--,DO30_DOCCORPO.*
FROM DO30_DOCCORPO
INNER JOIN DO11_DOCTESTATA ON
DO11_DITTA_CG18 = DO30_DITTA_CG18 AND
DO11_NUMREG_CO99 = DO30_NUMREG_CO99
WHERE DO11_TIPODOC = 24 AND DO11_STIPODOC = 5 
AND DO30_CODART_MG66 = 'IT AV'
AND DO30_SCPER1=0 AND DO30_SCPER2=0 AND DO30_SCPER3=0 AND DO30_SCPER4=0 AND DO30_SCPER5=0 AND DO30_SCPER6=0
AND DO30_MAGPER1 = 0 AND DO30_MAGPER2 = 0 AND DO30_SCIMP = 0 AND DO30_MAGIMP = 0
AND ( ((DO30_IMPNETSCP/DO30_QTA1)-DO30_PREZZO1) > 0.01 OR ((DO30_IMPNETSCP/DO30_QTA1)-DO30_PREZZO1) < -0.01 )
AND DO30_QTA1 <> 0
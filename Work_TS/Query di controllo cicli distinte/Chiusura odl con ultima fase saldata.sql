--UPDATE DO31_DOCCORPOORD SET DO31_INDSTATOCONS = 9
SELECT DO11_NUMDOC, DO31_INDSTATOCONS, *
FROM DO31_DOCCORPOORD
INNER JOIN DO11_DOCTESTATA ON
DO11_NUMREG_CO99=DO31_NUMREG_CO99
WHERE DO31_INDSTATOCONS < 9 AND DO11_DOCUM_MG36 = 'P-ODL'
AND (SELECT TOP 1 DO46_INDSTATOORD FROM DO46_DOCCORORDDET 
						WHERE DO46_NUMREG_CO99 = DO31_NUMREG_CO99 
						ORDER BY DO46_CODSEQFASE DESC
			) = 3
--AND DO11_NUMDOC = 7270
ORDER BY 1